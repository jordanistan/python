x = int(input("x: "))
y = int(input("y: "))

# Addition
print(f"{x} + {y} = {x + y}")

# Subtraction
print(f"{x} - {y} = {x - y}")

# Multiplication
print(f"{x} * {y} = {x * y}")

# Division
print(f"{x} / {y} = {x / y}")

# Floor Division
print(f"{x} // {y} = {x // y}")

# Exponentiation
print(f"{x} ** {y} = {x ** y}")

# Modulo
print(f"{x} % {y} = {x % y}")

